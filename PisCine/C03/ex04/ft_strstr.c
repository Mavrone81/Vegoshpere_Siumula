char	*ft_strstr(char *str, char *to_find)
{
	int	i;
	int	n;

	i = 0;
	n = 0;
	if (to_find[0] == '\0')
		return (str);
	while (str[i] != '\0')
	{	
		if (str[i] == to_find[n])
		{
			n++;
			if (to_find[n] == '\0')
			{
				return (&str[i - n + 1]);
			}
		}
		else
		{
			n = 0;
		}
		i++;
	}
	return (0);
}
